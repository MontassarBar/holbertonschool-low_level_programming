#include <stdio.h>
#include "3-calc.h"
/**
*op_add - Addition
*@a: var
*@b: var
*Return: a + b
**/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub - Subtraction
*@a: var
*@b: var
*Return: a - b
**/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul - Multiplication
*@a: var
*@b: var
*Return: a * b
**/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div - Division
*@a: var
*@b: var
*Return: a / b
**/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod - Modulo
*@a: var
*@b: var
*Return: a % b
**/
int op_mod(int a, int b)
{
return (a % b);
}
